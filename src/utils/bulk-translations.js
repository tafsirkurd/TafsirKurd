function setupHomeNavigation() {
    const homeLink = document.getElementById('homeLink');
    const navHome = document.getElementById('navHome');

    function scrollToTop(e) {
        e.preventDefault();
        window.scrollTo({ top: 0, behavior: 'smooth' });
    }

    if (homeLink) {
        homeLink.addEventListener('click', scrollToTop);
    }

    if (navHome) {
        navHome.addEventListener('click', scrollToTop);
    }
}

document.addEventListener('DOMContentLoaded', async () => {
    // Handle OAuth callback redirect
    if (window.location.hash && window.location.hash.includes('access_token')) {
        console.log('📍 OAuth callback detected on index.html, redirecting...');
        const redirectDest = sessionStorage.getItem('login_redirect_destination') || '/quran.html';
        // Keep the hash (access tokens) and redirect to intended page
        window.location.href = redirectDest + window.location.hash;
        return; // Stop processing this page
    }

    initPreloader();

    const yearElement = document.getElementById('year');
    if (yearElement) {
        yearElement.textContent = window.KurdishNumbers.toKurdish(new Date().getFullYear());
    }

    try {
        await loadWebsiteContent();

        // Refresh founder avatar cache
        try {
            if (window.supabase) {
                var avatarResult = await window.supabase
                    .from('site_settings')
                    .select('value')
                    .eq('key', 'founder_avatar_url')
                    .single();
                if (!avatarResult.error && avatarResult.data && avatarResult.data.value) {
                    localStorage.setItem('founder_avatar_url', avatarResult.data.value);
                    var avatarEl = document.getElementById('founderNavAvatar');
                    if (avatarEl && !avatarEl.querySelector('img')) {
                        var img = document.createElement('img');
                        img.src = avatarResult.data.value;
                        img.alt = 'سامان عبدالرحمن';
                        img.className = 'nav-dropdown-img';
                        while (avatarEl.firstChild) avatarEl.removeChild(avatarEl.firstChild);
                        avatarEl.appendChild(img);
                    }
                } else {
                    localStorage.removeItem('founder_avatar_url');
                }
            }
        } catch (e) { /* keep current state */ }

        // Load background images from database
        await loadBackgroundImagesFromDB();

        // Load hero card images from database
        await loadHeroCardImages();

        // Load testimonials
        await loadTestimonials();

        // Wait for background image to load, then hide preloader
        await setInitialBackground();
        if (window.hidePreloader) {
            window.hidePreloader();
        }

        startBackgroundRotation();

        // Initialize hero canvas AFTER Supabase is ready
        initHeroCanvas();

    } catch (error) {
        loadFallbackContent();
        // Still init canvas with default theme if Supabase fails
        initHeroCanvas();
        // Hide preloader even on error
        if (window.hidePreloader) {
            window.hidePreloader();
        }
    }

    // Safety timeout - hide preloader after 5 seconds max
    setTimeout(() => {
        if (window.hidePreloader) {
            window.hidePreloader();
        }
    }, 5000);

    // Load simple location tracker (doesn't depend on Supabase CDN)
    const locationScript = document.createElement('script');
    locationScript.src = '/utils/simple-location-tracker.js?v=' + Date.now();
    document.body.appendChild(locationScript);

    new FloatingActionButtons();
    setupHomeNavigation();
    setupScrollAnimations();
    setupShowcaseScrollBorder();
    setupHeroWordReveal();
    setupMobileMenu();
    setupNavScrollEffect();
    setupContactForm();

    // Init daily content (loads JSON + episodes, starts 5-min timer)
    try {
        await initDailyContent();
    } catch (e) {
        console.warn('Daily content init failed:', e);
    }

    if (window.supabase) {
        const contentRefreshInterval = setInterval(loadWebsiteContent, 300000);
        window.addEventListener('beforeunload', () => {
            if (contentRefreshInterval) clearInterval(contentRefreshInterval);
            if (_dailyTimerInterval) clearInterval(_dailyTimerInterval);
        });
        // Load dynamic content from database
        loadSocialStats();
        loadFeatures();
        loadScheduleItems();
    }

    // Track geographic visit - DISABLED (not needed)
    // trackGeoVisit();

    // Register Service Worker for offline support
    if ('serviceWorker' in navigator) {
        navigator.serviceWorker.register('/service-worker.js?v=506')
            .then(registration => {
                console.log('✅ Service Worker registered successfully:', registration.scope);
            })
            .catch(error => {
                console.log('❌ Service Worker registration failed:', error);
            });
    }
});

