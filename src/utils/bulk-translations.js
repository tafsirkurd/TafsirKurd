        // Initialize Supabase with secure credentials
        async function initializeSupabase() {
            try {
                // Fetch credentials from secure endpoint
                const response = await fetch('/config');

                if (!response.ok) {
                    throw new Error('Failed to fetch auth configuration');
                }

                const config = await response.json();

                if (!config.supabaseUrl || !config.supabaseKey) {
                    throw new Error('Invalid auth configuration');
                }

                // Initialize Supabase client
                supabaseClient = window.supabase.createClient(config.supabaseUrl, config.supabaseKey);
                window.supabaseClient = supabaseClient;
                console.log('✅ Supabase initialized securely');
                return true;

            } catch (error) {
                console.error('❌ Failed to initialize Supabase:', error);
                showMessage('خەتایەک ڕوویدا لە دامەزراندنی سیستەم. تکایە دواتر هەوڵ بدەرەوە.', 'error');
                return false;
            }
        }

        // Form switching — smooth cross-fade transitions
        let _formSwitching = false;
        function showForm(formName) {
            if (_formSwitching) return;
            console.log('Switching to form:', formName);

            const currentActive = document.querySelector('.form-section.active');
            const targetForm = document.getElementById(formName + 'Form');
            if (!targetForm || targetForm === currentActive) return;

            // Update tabs (hide tabs for forgot/otp)
            const tabsContainer = document.getElementById('authTabs');
            if (formName === 'forgot' || formName === 'otp') {
                tabsContainer.style.display = 'none';
            } else {
                tabsContainer.style.display = 'flex';
                document.querySelectorAll('.tab').forEach((tab, index) => {
                    tab.classList.remove('active');
                    if ((formName === 'signin' && index === 0) || (formName === 'signup' && index === 1)) {
                        tab.classList.add('active');
                    }
                });
            }

            clearMessage();

            // Cross-fade: fade out current, then fade in target
            if (currentActive) {
                _formSwitching = true;
                currentActive.classList.remove('active');
                currentActive.classList.add('fading-out');
                setTimeout(() => {
                    currentActive.classList.remove('fading-out');
                    currentActive.style.display = 'none';
                    targetForm.style.display = '';
                    targetForm.classList.add('active');
                    _formSwitching = false;
                }, 200);
            } else {
                targetForm.classList.add('active');
            }

            // Update hero panel context if applicable
            updateHeroForForm(formName);
        }

