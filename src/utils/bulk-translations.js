/**
 * Import all translations to Supabase
 * Run this from browser console on any page with Supabase initialized
 */
async function importAllTranslations() {
    const config = await fetch('/config').then(r => r.json());
    const supabase = window.supabase.createClient(config.supabaseUrl, config.supabaseKey);

    let added = 0, skipped = 0, errors = 0;

    for (const t of BULK_TRANSLATIONS) {
        try {
            // Check if exists
            const { data: existing } = await supabase
                .from('kurdish_translations')
                .select('id')
                .eq('key_id', t.key_id)
                .single();

            if (existing) {
                skipped++;
                continue;
            }

            // Insert new
            const { error } = await supabase
                .from('kurdish_translations')
                .insert(t);

            if (error) {
                console.error(`Error: ${t.key_id}`, error);
                errors++;
            } else {
                added++;
            }
        } catch (e) {
            errors++;
        }
    }

    console.log(`✓ Import complete: ${added} added, ${skipped} skipped, ${errors} errors`);
    return { added, skipped, errors };
}

// Export for use
window.BULK_TRANSLATIONS = BULK_TRANSLATIONS;
window.importAllTranslations = importAllTranslations;
